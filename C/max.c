#include <stdio.h>
//判断大小端
//假设右边是高地址，左边是低地址
int main()
{
	int a = 1; // 0x0000 0001
	//如果是大端 低位字节放到高地址，高位字节放到低地址
	//00 00 00 01
	//如果是小端 高位字节放到高地址，低位字节放到低地址
	//01 00 00 00
	char* p = (char*)&a; //字符指针只读1个字节，读8位
    //如果是大端存储，则p读取的值是0
    //如果是小端存储，则p读取的值是1
	printf("%d\n", *p);
    if (*p == 1)
		printf("小端\n");
	else
		printf("大端\n");
	return 0;
}
